#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	char	*buff;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	buff = ft_calloc(end - start + 1, sizeof(char));
	if (buff == NULL)
		return (NULL);
	ft_strlcpy(buff, (s1 + start), end - start + 1);
	return (buff);
}
